    private void showBook() {
        bookModels = new BookController().GetBooks();
        tablebook.setRowCount(0);
        for (BookModel bookModel : bookModels) {
            tablebook.addRow(new Object[]{
                bookModel.getBookID(), bookModel.getAuthorName(), bookModel.getQuantity()
            });
        }
    }

    private void showAccount() {
        accountModels = new AccountController().GetAccounts();
        tableaccount.setRowCount(0);
        for (AccountModel accountModel : accountModels) {
            tableaccount.addRow(new Object[]{
                accountModel.getAccountName(), accountModel.getPhoneNo()
            });
        }
    }

