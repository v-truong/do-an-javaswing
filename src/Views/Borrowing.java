    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm");

        String borrowedDate = borrowedDateInput.getText();
        LocalDateTime data1 = LocalDateTime.parse(borrowedDate, formatter);
       
        String returnDate = returnDateInput.getText();
        LocalDateTime data2 = LocalDateTime.parse(returnDate, formatter);
  
        int accountID = accountModels.get(selectedIndex1).getAccountID();
        int bookID = bookModels.get(selectedIndex2).getBookID();
        
        
        Boolean isSuccess = new BorrowingController().CreateBorrowing(accountID, bookID, data1, data2);
        if (isSuccess == true) {
                JOptionPane.showMessageDialog(this, "Tạo mới dữ liệu mượn sách thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Tạo mới dữ liệu mượn sách thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_saveBtnActionPerformed

