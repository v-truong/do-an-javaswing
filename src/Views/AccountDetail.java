    private void saveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveBtnMouseClicked
        // TODO add your handling code here:
        int accountID = 0;
        if (account != null && account.getAccountID() != 0) {
            accountID = account.getAccountID();
        }
        String username = usernameInput.getText();
        String password = pwdInput.getText();
        String account = accountNameInput.getText();
        String phoneNumber = phoneNoInput.getText();
        String description = descriptionInput.getText();
        int roleID = 0;
        for (RoleModel roleModel : roleModels) {
            if (roleModel.getRoleName() == roleSelect.getSelectedItem()) {
                roleID = roleModel.getRoleID();
            }
        }

        Boolean isSuccess;
        if (isEdit == true) {
            isSuccess = new AccountController().UpdateAccount(accountID, account, username, password, phoneNumber, roleID, description);
        } else {
            isSuccess = new AccountController().CreateAccount(account, username, password, phoneNumber, roleID, description);
        }

        if (isSuccess == true) {
            this.dispose();
            Account at = new Account();
            at.setVisible(true);
        }
    }//GEN-LAST:event_saveBtnMouseClicked

