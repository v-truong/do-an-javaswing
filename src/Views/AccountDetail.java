    private void getBorrowing(int id) {
        borrowingModels = new BorrowingController().GetDetailBorrowingByAccount(id);
        tableModel.setRowCount(0);
        for (BorrowingModel borrowing : borrowingModels) {
            String status;
            if (borrowing.getStatus() == 1) {
                status = "Đang mượn";
            } else {
                status = "Đã trả";
            }
            tableModel.addRow(new Object[]{
                borrowing.getBookID(), borrowing.getBookName(), borrowing.getBorrowedDate(), borrowing.getReturnDate(), status
            });
        }
    }

    private void getAccountDetail(int id) {
        account = new AccountController().GetAccountDetail(id);
        int roleID = account.getRoleID();
        for (RoleModel roleModel : roleModels) {
            if (roleModel.getRoleID() == roleID) {
                roleSelect.setSelectedItem(roleModel.getRoleName());
            }
        }
        accountNameInput.setText(account.getAccountName());
        usernameInput.setText(account.getUsername());
        pwdInput.setText(account.getPassword());
        phoneNoInput.setText(account.getPhoneNo());
        descriptionInput.setText(account.getDescription());
    }

    private void getRoles() {
        roleModels = new RoleController().GetRoles();
        rBoxModel.setSelectedItem(0);
        for (RoleModel roleModel : roleModels) {
            rBoxModel.addElement(roleModel.getRoleName());

        }

    }

